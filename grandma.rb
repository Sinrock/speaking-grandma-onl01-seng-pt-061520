def speak_to_grandma(greeting)

    if greeting == "I LOVE YOU GRANDMA!"
      return "I LOVE YOU TOO PUMPKIN!"
    elsif greeting == greeting.downcase
  		return "HUH?! SPEAK UP, SONNY!"
    elsif greeting == greeting.upcase
      return "NO, NOT SINCE 1938!"
    else
  		return "NO, NOT SINCE 1938!"
    end
end
